#include "libft.h"

int	ft_atoi(const char *str)
{
	int re;
	int s;

	s = 1;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if ((*str == '-' || *str == '+') && (*(str + 1) >= 48 && *(str + 1) <= 57))
	{
		if (*str == '-')
			s = -1;
		str++;
		re = 0;
		while (*str >= 48 && *str <= 57)
		{
			re *= 10;
			re += (*str) - 48;
			str++;
		}
		return (re * s);
	}
	return (0);
}
